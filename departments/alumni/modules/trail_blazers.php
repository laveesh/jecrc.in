  	<p>JECRC believes that entrepreneurship is not just about starting companies, but a pathway towards
  	India’s socio-economic development. Our vision is to make students and faculty ‘entrepreneurial’ in
  	every work that they do. We hope to enable them to solve global challenges as we see students as the
  	greatest capital and intend to nurture them, provide them with opportunities for excellence.</p>
	<p>The Incubation centre by JECRC strives hard to make the student&#39;s entrepreneurial journey smooth and
	infuse the spirit of entrepreneurship among others. And the best example of the support that JECRC
	provides by always being the cushion in the entrepreneurial journey of its students can be very well
	seen from the larger number of alumni who have gone out of the box and started their own start ups
	which deal with a variety of fields ranging from manufacturing of Jaipur Belt to being the producer of
	movies. With more than 150 alumni start ups JECRC stands high to provide guidance to it’s present
	students by organizing talks between present CEO&#39;s and the budding Founders, that quenches a lot of
	budding answers and gives birth to new ideations simultaneously. What can be better than hearing it
	from the ones who have been in the same boat and sailed it to nooks of the world yet to be explored by
	others.</p>

