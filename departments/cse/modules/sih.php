	<div class="sectionContent">	
		<p class="px-3">
	JECRC was one of the only 28 colleges from all over the country to have been shortlisted as the nodal centre for conducting the Grand Finale of Smart India Hackathon 2018. JECRC, Jaipur was assigned the nodal Centre for Ministry of Information and Broadcasting. Over 400 students of 50 teams and 25 subject experts, judges and technical officers from all over the country came to JECRC campus.There were total 16 problem statements of Ministry of I& B for which 50 teams from all over India participated to provide their real solutions.

</p>
	<p class="px-3">The inauguration ceremony of SIH 2018 @ JECRC nodal Centre, Jaipur was graced by the presence of Shri A. P. Mittal, Member Secretary, AICTE, along with the judges and Nodal officer from ministry of I & B.The coding session started as per the schedule.
</p>
	<p class="px-3">The AICTE representatives, along with the judges and Nodal Officer, mentored the students for their respective problem statements in various mentoring/training sessions on 30th and 31st March. These teams were evaluated by a total of 21 judges from the Ministry as well as renowned software companies. 
</p>
	<p class="px-3">Hon'able Prime Minister Sh. Narendra Modi himself spoke to the participating students and organizers at all 28 nodal centres expressing his faith in the long terms benefits of this nationwide technological revolution. 
</p>
	<p class="px-3">The valedictory session which was graced by the presence of Col. Rajyavardhan Singh Rathore, State Minister (I & B), Cabinet minister (Ministry of Youth Affairs and Sports) and Shri R.S. Rathore, Director, AICTE. JECRC got an appreciation letter from Chief Minister of Rajasthan, Mrs. Vasundhara Raje and also from Mrs. Smriti ZubinIrani, Cabinet Minister of Information and Broadcasting and Textile Ministry.
</p>
	<p class="px-3">After various evaluation rounds, 10 teams were shortlisted for the power judgment round in which they presented their solutions in front of the panel.  6 teams were finally selected and were awarded trophies winner was Linguistic Panda from Delhi,The Techie Tribes from Tamilnadu was Ist Runner Up, OceanSix from Udaipur was IInd Runner Up, Local host from Delhi got Persistent Innovation award, Hackers Troops from Chennai got KPIT Inspiration award and Inferno from Odisha got Deloitte Innovation awards in the valedictory session.
</p>
	<p class="px-3">Further, nine teams from JECRC were also shortlisted to participate in SIH 2018 for different ministries at different nodal centres. Also ministries have approached teams from JECRC to have internships with the ministries, for at least one year and develop their solutions into workable models.
</p>
	
