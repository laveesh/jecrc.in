		<div class="col-md-6">
			<p class="px-3 ">To fulfill our PM’s vision, MHRD, All India Council for Technical Education (AICTE), Inter Institutional Inclusive Innovation Center (i4C), and Persistent Systems organized Smart India Hackathon 2017, a unique initiative to identify new and disruptive digital technology innovations for solving the challenges faced by our country. The initiative was a huge success. To reiterate the efforts towards our PM’s vision, all together proposed the 2nd edition of this initiative – Smart India Hackathon 2018. Smart India Hackathon 2018 is being organized and had 2 sub-editions – Software as well as Hardware.
</p>

