	<p class="px-3">The problems were taken from various ministries, departments as well as from JECRC Foundation itself and the ideas of the students were finalized after going through two rigorous scrutiny tests. Out of 300 teams registered, 152 teams were finally selected and 768 students converted their ideas into new developments.</p>
	<p class="px-3">The inauguration ceremony of this event was graced by the presence of Prof. Dileep N. Malkhede, Advisor, AICTE and Prof. N.P. Kaushik, Vice Chancellor, RTU, Kota. Also, we were honoured to have Mr. Mohammad Imran Khan, Web developer and Ms. Nayani Nasa, Startup Manager, Invest India with us.</p>
	<p class="px-3">The coding session started and 12 judges along with 123 mentors fertilized the raw brains of the students with their experiences. Each judge was allotted 14 teams to evaluate. After 2 rounds of judging, finally 47 teams were ready for the power judging round.</p>
	<p class="px-3">Meanwhile, some recreational activities were also organised to regenerate the energy and enthusiasm of the students. In the mid night of Jan 10th, 2018, a huge bonfire was lit in the central lawn of JECRC Campus. It created a soothing night time ambience and the zeal returned in everyone to work throughout the night.</p>
	<p class="px-3">The valedictory session was organised on Jan 11th, 2018 and was graced by the presence of Dr. Ravi Kumar Maddila, Assistant Professor, Department of ECE, MNIT; Dr. Sanjeev Agarwal, Associate Professor, Department of ECE, MNIT and Dr. Ashish Kr. Ghunawat, Assistant Professor, Department of ECE, MNIT.</p>
