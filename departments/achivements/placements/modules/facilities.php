	<div class="sectionHeading">Training</div>
	<p>
		We provide Pre Placement training to our students to enable them to excel in campus recruitment drives. We engage
		Professional trainers to train them in preparing for various types of aptitude tests . Mock tests are provided for
		various companies and based on results additional preparation is done. Our departments prepare them for technical
		interviews and senior advisors having experience in Industry provide practice in Group discussions , Managerial and
		HR Interviews. Mock drills are also conducted to provide students with a real time simulation of an actual campus
		drive. The students are assigned experienced faculty Mentors to guide them through out the year and keep trace of
		their performance.
	</p>
