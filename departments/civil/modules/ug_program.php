    <p>The objective of Department of CE for the undergraduate programe is:</p>
        <ol>
            <li>To equip the students with strong foundation of basic sciences and domain knowledge of CE, so that they are able to creatively apply their knowledge in field to find solution of problems arising in their career path in field.
            <li>To induce the habits of lifelong learning in order to continuously enhance overall performance.
            <li>Students are able to communicate their ideas clearly and concisely so that they can work in team as well as an individual.
