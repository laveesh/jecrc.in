<p>The Department of Civil Engineering is affiliated to Rajasthan Technical University and offers Under-Graduate programmes.</p>
<p>The CE Department practices the Outcome Based Education (OBE) approach for teaching-learning process. This is a learning-centric approach rather than traditional teaching-centric approach to education. This approach focuses on the attributes the students should possess after the completion of the programme through the following key questions:-</p>

<ol>
<li>At the end of the course, what should a student be able to do?</li>
<li>How can the faculty and staff help the student in gaining this ability?</li>
<li>How will we measure if the students have gained this ability?</li>
<li>How to involve all the stakeholders viz. students, faculty, alumni, employers, industry, parents and society, for continuous improvement of the programme?</li>
</ol>

<p>Under OBE model, the quality of teaching is measured by the quality of learning, defined as outcomes, that takes place. Programme Educational Objectives (PEOs) are formulated to align with the vision and mission of the Department. Programme Outcomes (POs) are defined to achieve PEOs which are further aligned to Course Outcomes (CO) of each subject. Vision, Missionand PEOs are the long-term outcomes whereas POs and COs are the short-term outcomes.</p>

