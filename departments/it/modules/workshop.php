					The idea of gaining knowledge through specialized analysis of mass data started with data collection in the 1960s, and has steadily increased both in the amount of data processed and the sophistication of questions businesses try to answer. <br>

					Through this progression from static to dynamic and now to proactive provision of information, knowledge discovery through databases (KDD) still has the goal to extract useful intelligence from this data by using database and data management aspects like cluster analysis, classification or regression. <br>

					Early adopters of data mining were certain sectors that already had an affinity to data, such as the financial services industry and insurance companies. Retailers followed soon, using it for tracking inventory and various forms of customer relations management. Now utility companies use smart meters to predict energy consumption, and health care providers use RFID chips in name tags to track how often doctors wash their hands during rounds to help prevent the spreading of disease.
