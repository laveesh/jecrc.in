<div class="sectionHeading">Undergraduate Program</div>
<div class="card-body sectionContent">
<p>The objective of Deparetement of Electronics and Communication Engineering for the undergraduate program is:</p>
<ol class="sectionList">
<li>To equip the students with strong foundation of basic sciences and domain knowledge of Electronics and Communication Engineering, so that they are able to creatively apply their knowledge to design solution of problems arising in their career path.</li>
<li>To induce the habits of lifelong learning in order to continuously enhance overall performance.</li>
<li>Students are able to communicate their ideas clearly and concisely so that they can work in team as well as an individual.</li>
<li>To make the students responsive towards the ethical, social, environmental and economical growth of the society</li>

<h3 class="sectionSubHeading">The Departement of Electronics and Communication Engineering</h3>
<p>Eligibility Criteria:</p>
<p>Should have passed 10+2 examination with Physics and Mathematics as compulsory subjects along with one of the following subjects:</p>
<ol class="sectionList">
<li >Chemistry</li>
<li >Biotechnology</li>
<li >Computer Science</li>
<li >Biology</li>
</ol>
<p>Candidates have to appear in the State JEE Exam apart from the qualification given above to get admission.</p>
<p>The Institute is affiliated to Rajasthan technical universityTechnical University (RTU), Kota, Rajasthan. B.Tech course is duly approved by the All India Council for Technical Education (AICTE), Government of India.</p>
<p>Jaipur Engineering College and Research Centre is affiliated to Rajasthan Technical University, Kota. So, the program curriculum is as per the scheme and syllabus described by RTU, Kota. The Curriculum maintains a balance in the composition of Basic Science, Humanities and Professional Ethical courses, Computer Programming based subjects along with Basic and Advanced branch related subjects. </p>
