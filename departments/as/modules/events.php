					<li><strong>Objectives:</strong>
						<p>Department of Mathematics organized an International conference on Mathematical Modeling & Computing on April, 6-7 2018 at JECRC. The conference covered the different tracks including Mathematical Modeling, Computing, Statistics, Image Processing, Relativity & Cosmology, Fuzzy Logic, Fluid Dynamics, Fractional calculus, Integral Transforms etc. 
                        The Conference started with Inauguration in the college Auditorium at 9:30 am on 6th April 2018, where speakers and guests were welcomed with mementoes as token of gratitude towards them.</p>
                       <ul>
                        The Key Note Speakers for the conference were:
                       <li> Prof. P. Chaturani(Retd. from IIT Mumbai)</li>
                       <li> Prof. Peeyush Chandra(Retd. from IIT Kanpur)</li>
                       <li>Prof. K.S. Nisar(PSAU, Saudi Arabia, Delivered a talk on skype)</li>
                       </ul>
                        Total 5 sessions of paper presentations held during two days of the conference ICMMC-18. 
                        Session chairs for the individual session are Prof. S.P Gupta(Retd. Prof. from UOR), Prof. Swati Chande(IIIM , Jaipur), Prof. Paresh Vyas(UOR, Jaipur), Dr. Ritu Goyal(MNIT, Jaipur), Dr. Pranay Goswami(Ambedkar University, Delhi)
                    </li>
					<li><strong>Outcome:</strong>
						<p>The conference received about 100 research papers out of which 40 were selected for presentation after undergoing rigorous review process. Board of reviewers of ICMMC include experts from India and abroad. Participants from all over the country presented their papers in front of the expert panel and session chairs followed by small question answer round which helped participants to improve their reserch. 
