<div class="sectionContent">
	<p>JECRC, Jaipur organized a session on Alumni Interaction Program. This session is delivered by Mr. Shubham Khaitan , an alumni of 2017 batch, is joining IOCL. He addressed 7th semester electrical students on 29th July 2017 from 1:30 to 3:30 PM in BLG-13.</p>
	<p>
		This session started with his brief introduction by Mr. Atul Kulsherstha , Head of the department, of EE department. Mr. Shubham Khaitan shared his experience of being placed in IOCL, ONGC and IISC, Bangalore. The objective of this session was to provide guidance to final year students for their placements. He began his talk by telling students how to prepare for GATE. Then he spoke about how to present oneself in the interview and in the organization.
	</p>
		<p>He then shared with the students about the interview of being good at technical skills along with soft skills and how they should present the project to the interviewer.  Mr. Shubham Khaitan then explained the kind of question that students could ask the interviewer.  He motivated the students to focus more on improving their technical skills. The session was very interactive. He then answered the various queries asked by the students about his performance in interview.
		</p>
		<p>
		About 100 students attended this session. The vote of thanks was proposed by Mr. Rahul Kumar Malee, faculty of EE department for having delivered an interactive and information session.</p>

