<p >Bachelor of Technology in Mechanical Engineering or B.Tech. (ME) is a full-time undergraduate programme, affiliated to Rajasthan Technical University(RTU), with a total duration 4 years, each year comprising of two semesters. It is one of the most sought-after programmes. All admissions to this programme are through Rajasthan Engineering Admission Process(REAP).</p>
<p >The course curriculum includes core courses along with professional as well as open elective courses. Courses from humanities and social, basic and engineering sciences are also given due weight as per standard norms.</p>
