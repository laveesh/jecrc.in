		<p>Our Hon’ble Prime Minister, Shri Narendra Modi, envisages a Digital India to bridge the
		digital divide in our country and further promote digital literacy in order to make
		development a comprehensive mass movement and put governance within everyone’s reach
		in India. In order to work towards our PM’s vision, MHRD, All India Council for Technical
		Education (AICTE), Inter Institutional Inclusive Innovation Center (i4C), and Persistent
		Systems organized Smart India Hackathon 2017, a unique initiative to identify new and
		disruptive digital technology innovations for solving the challenges faced by our country. The
		initiative was a huge success. To reiterate the efforts towards our PM’s vision, all together
		proposed the 2nd edition of this initiative – Smart India Hackathon 2018. Smart India
		Hackathon 2018 is being organized and had 2 sub-editions – Software as well as Hardware.
		45+ union ministries and departments and 18 state governments had joined this grand
		initiative, making it much bigger than the previous edition. Smart India Hackathon 2018 is a
		non-stop digital product development competition, where 340 problems are posed to
		technology students for innovative solutions. It harnesses creativity &amp; expertise of students,
		Sparks institute-level hackathons, builds funnel for ‘Startup India’ campaign, provides
		opportunity to citizens to give innovative solutions to India’s daunting problems.
		JECRC was one of the only 28 colleges from all over the country to have been shortlisted as
		the nodal centre for conducting the Grand Finale of Smart India Hackathon 2018. JECRC,
		Jaipur was assigned the nodal Centre for Ministry of Information and Broadcasting. Over 400
		students of 50 teams and 25 subject experts, judges and technical officers from all over the
		country came to JECRC campus.</p>
<p>For having a smooth conduction of event the JECRC established a coordination committee
with the officials from PIB, Jaipur. After rigorous discussions with the nodal officer from
Ministry and other officials, all the required preparations were finalized. Working closely
with ministry was great experience for all staff and students of JECRC.
There were total 16 problem statements of Ministry of I &amp; B for which 50 teams from all
over India participated to provide their real solutions. The teams were accompanied by their
mentors from industry. Teams from all over India congratulated JECRC team on such a
successful event. They also appreciated the communication which was established with them
prior to the main event as all the important information was communicated to them well in
time.</p>
<p>The inauguration ceremony of SIH 2018 @ JECRC nodal Centre, Jaipur was graced by the
presence of Shri A. P. Mittal, Member Secretary, AICTE, along with the judges and Nodal
officer from ministry of I &amp; B. The coding session started as per the schedule.
</p>
<p>The AICTE representatives, along with the judges and Nodal Officer, mentored the students
for their respective problem statements in various mentoring/training sessions on 30 th and 31 st
March. These teams were evaluated by a total of 21 judges from the Ministry as well as
renowned software companies.
</p>
<p>Hon&#39;able Prime Minister Sh. Narendra Modi himself spoke to the participating students and
organizers at all 28 nodal centres expressing his faith in the long terms benefits of this
nationwide technological revolution.
The two-day schedule included a lot of recreational activities for the participants as well like
Yoga Sessions, Zumba Dance Sessions &amp; Red Bull Hour. These participants were provided
lodging facilities within campus the in hostels of JECRC. More than 100 students of JECRC
and around 64 faculty members were part of the organizing team that made this event
possible. This event witnessed a footfall of many senior government officials and AICTE
officials.</p>
<p>The valedictory session which was graced by the presence of Col. Rajyavardhan Singh
Rathore, State Minister (I &amp; B),Cabinet minister (Ministry of Youth Affairs and Sports) and
Shri R.S. Rathore, Director ,AICTE. It was great and proud moment in the history of JECRC
to listen to honorable minister and share experiences with them. While addressing the
gathering of students he said that “Life goes on by hard work and will power and if you go
with this mantra you will get success in life&quot;. He congratulated all the participant teams as
well as Nodal Centre for successful organization of SIH 2018.</p>
<p>JECRC got an appreciation letter from Chief Minister of Rajasthan, Mrs. Vasundhara Raje
and also from Mrs. Smriti Zubin Irani, Cabinet Minister of Information and Broadcasting and
Textile Ministry.</p>
<p>After various evaluation rounds, 10 teams were shortlisted for the power judgment round in
which they presented their solutions in front of the panel. 6 teams were finally selected and
were awarded trophies winner was Linguistic Panda from Delhi ,The Techie Tribes from
Tamilnadu was Ist Runner Up, OceanSix from Udaipur was IInd Runner Up, Local host from
Delhi got Persistent Innovation award, Hackers Troops from Chennai got KPIT Inspiration
award and Inferno from Odisha got Deloitte Innovation awards in the valedictory session.
Over 100 students and all faculty members of JECRC took the responsibility for the
successful execution of this national mega event. JECRC Team worked very hard with full
sincerity, dedication, devotion and coordination to make this event successful.
Further, nine teams from JECRC were also shortlisted to participate in SIH 2018 for
different ministries at different nodal centres. Also ministries have approached teams from
JECRC to have internships with the ministries, for at least one year and develop their
solutions into workable models.</p>
