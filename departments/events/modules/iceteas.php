<p>International Conference on Emerging Trends in Expert Applications &amp; Security (ICETEAS 2018) held on
Feb 17-18, 2018 at JECRC, Jaipur, technically sponsored by Springer – AISC, BJIT (Published by Springer,
Nature), IGI-Global in association and UGC approved IJRRA along with AICTE, DST, RTU, Computer
Society of India (CSI) &amp;Association of Computing Machinery (ACM). The conference addressed
innovative advancements in Expert Applications &amp; Security Issues therein with specific reference to the
area of Computing while providing an opportunistic forum and vibrant platform for researchers and
industry practitioners to exchange of state-of-the-art knowledge gained from their original research
work and practical developments on specific new challenges, applications and experiences, to establish
business or research relations, and to find global partners for future collaboration on emerging issues.</p>

<p>ICETEAS 2018 received around 367 submissions from around 639 authors from 10 different countries
such as the UK, US, Netherlands, Italy, Japan, China, Korea, Thailand, Vietnam, Taiwan and many more.
Each submission has been through the plagiarism check and was rigorously reviewed by at least three
reviewers with an average of 2.07 per reviewer. Even some submissions have more than three reviews.
On the basis of these reviews 120 research papers were presented during the 2 day conference in four
parallel sessions of five tracks, namely, Expert Applications and Artificial Intelligence, Information and
Application Security, Advanced Computing, Multimedia Applications in Forensics, Security &amp; Intelligence,
Advances in Web Technologies, with an acceptance rate of 17%.</p>
