<p>With a strong industry connect; JECRC foundation has a proven track record of conferring excellent campus placements and industrial training to the students in many reputed industries. JECRC builds a solid foundation in terms of knowledge and personality for its students to perform consistently and excel at every stage in their career. At the end of the sixth semester, students are sent to industries for industrial training of 60 days to get them hands-on experience of what awaits them in a professional environment. Along with this, specialised coaching sessions are conducted for a month to prepare students for the aptitude tests, group discussions and personal interviews. This year the foundation has an excellent track record of placement so far. More than 86 companies have visited and made 1250+ offers. The average and highest package rolled out are Rs 3.4 lacs and Rs 8 lacs p.a. respectively, across the sectors like IT/I TeS, Manufacturing, Infrastructure, Product Development , Communication Technology, Automobile, Academia and Hospitality. Reputed Companies like Accenture, Amazon, Capgemini, HP, Anora Labs, AppPerfect, TCS, Adani Group, Tata Technologies, Bosch, First American Company, Patil Group, Newgen, Pinnacle, SLK Technology (Kirslokar Group), Zycus, Capital Via, Appirio, Sopra , Avon Hewitt , Tractor India Ltd . , KAI Manufacture, Hexaview Technology, JusPay, Ericsson, Genpact, Mindtree picked up large number of JECRC students. JECRC also has a proud representation in the Indian Armed Forces. Hewlettopackerd , Bridgestone Tyres , J.K Cement Works, Kirloskar Group, FEV India Pvt Ltd. are among the first to visite any private institute in Rajasthan region
CORE Industries This year JECRC has proven its worth by inviting giants from the Core Sector and made a different stand, as these companies mostly visit IITs and NITs only. Core Companies like JK Cement , Bridgestone , FEV India, Ericsson , Tractors India Limit e d , Ashiana Housing , TATA Technologies, ITC Limited, Adani Power, Pinnacle Infotech, Kirloskar Group, Perto etc., have given our students the chance of working in some of the best core industries in the world, giving them a one in a lifetime opportunity to shape their career. Internship / Training Opportunities by College JECRC is not only committed towards the best placements for its students but also, gives due importance to the internship and training vertical for the pre-final year students. Considering this fact, the JECRC Foundation, has taken another initiative this year by establishing a new cell that functions for bringing internships to pre-final year students. This vertical dedicatedly works for finding new internship opportunities for the students, bringing the best possible opportunities for its budding engineers. Soft Skill Training Specialized soft-skill classes, personal interviews and technical sessions are organized to prepare students for campus placement. A dedicated faculty has been separately assigned the intricate task of developing communication skills right from the first semester. As a result, in the previous year almost all of our students got placed in reputed companies through campus placements. Outset Group The institution believes in holistic development of the students and avails all available resources towards the achievement of 100% placement. JECRC Management has taken a unique initiative of starting a vertical exclusively for placement of students with less than 60% marks and many backlogs. This group works for providing training, motivation and opportunities to students who face difficulties with campus placement. With the untiring efforts of this group about 320 students have been placed in 25 good companies with an average package of 2.1 LPA and maximum package of 6 LPA. Corporate Contest Participation JECRC students are regular participants in various contests organized by companies, from time to time. Some of them are national and state winners in which they have also won internships opportunities and job offers. JECRC has initiated a separate cell for these contests with 3 dedicated 
coordinators to enhance students interest, participation and to guide them for maximum learning.</p>
