fforts of this group about 320 students have been placed in 25 good companies with an average package of 2.1 LPA and maximum package of 6 LPA. Corporate Contest Participation JECRC students are regular participants in various contests organized by companies, from time to time. Some of them are national and state winners in which they have also won internships opportunities and job offers. JECRC has initiated a separate cell for these contests with 3 dedicated 
