	<div class="sectionContent">
		<p class="py-2">In this fast pacing world running behind the power of technology, there is a loss of awareness of Self and the Supreme power. The meaning of life has changed its  definition from happiness to luxury. A pious place is created in JECRC to rejuvenate and re-establish the lost definition of Self and broken connection with the Supreme. It has a sound proof ‘Meditation Room’ developed as silence zone for meditation and self-contemplation. It also includes a Spiritual Library, with collection of best selling spiritual and inspirational books. Regular classes are conducted in Wisdom Hall which is designed as a smart classroom. A Research Laboratory for conducting research on meditation is equipped with EEG, EMG, Karadascan, Aura Scanning and other health monitoring devices. Mr. Mukesh Agarwal, Ms. Chitra Khandelwal and Ms. Aakanksha Desai are providing insight for the accomplishment of objectives of the Spiritual Cell.</p>
